public class Child {
    private String mName;
    private int mImage;

    public Child(String name){
        this.mName = name;
    }

    public String getName() {
        return mName;
    }

    public void setName(String Name) {
        this.mName = Name;
    }

    public int getImage() {
        return mImage;
    }

    public void setImage(int Image) {
        this.mImage = Image;
    }
}
